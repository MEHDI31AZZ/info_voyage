<h4 class="font_4" style="font-size:36px">1. Route de la soie d’Asie Centrale</h4>
<p class="font_8" style="font-size:15px">
Région qui avait connu la richesse grâce aux voyageurs et au commerce, la route de la soie d’Asie Centrale attire à nouveau les regards du monde. Les villes anciennes, les bazars animés et les paysages sauvage de l’Asie Centrale attirent de plus en plus de visiteurs en quête d’aventure sur une des routes historiques les plus connues. Khirgizistan, Tadjikistan et Ouzbékistan sont maintenant ouverts sans visa ou avec un visa électronique à la plupart des citoyens du monde, et la région prépare un visa unique pour la "Route de la soie". Dans le même temps, d’énormes investissements dans les transports et les infrastructures, le plus souvent sous l’égide de la Chine et de sa nouvelle route de la soie – rend la version moderne de cet itinéraire plus accessible que jamais. </p>
<p class="font_8" style="font-size:15px">​</span></p>

<h4 class="font_4" style="font-size:36px">2. Les Marches, Italie</h4>
<p class="font_8" style="font-size:15px">
Les doublures vont enfin tenir la vedette. Si le rôle principal est souvent dévolu à la superstar voisine, la Toscane, la région italienne des Marches possède un répertoire tout aussi complet. On y trouve aussi des villages serrés au sommet des collines, des festivals gastronomiques gourmands, des splendides palais Renaissance. Sa campagne qui ondule et ses plages attirantes ont autant de panache, mais avec l’avantage de grands sites moins connus. En 2020, les projecteurs se tourneront ici vers Urbino (Urbin), la ville la plus pittoresque des Marches, où on célébrera le 500e anniversaire de la mort du grand peintre de la Renaissance, l’enfant du pays, Raphaël.
</p>
<p class="font_8" style="font-size:15px">​</span></p>

<h4 class="font_4" style="font-size:36px">3. Tōhoku, Japon</h4>
<p class="font_8" style="font-size:15px">
Le Japon va vibrer en 2020 quand le monde se retrouvera à Tokyo pour les Jeux olympiques d’été, et il n’y a peut-être pas de région plus désireuse de participer à la fête que le Tōhoku. Se remettant peu à peu du séisme et du tsunami dévastateurs de 2011, cette région peu visitée du pays travaille dur pour rouvrir ses voies de circulation, développer de nouveaux grands sentiers de randonnée et reconstruire et améliorer ses équipements touristiques. Déjà connu au Japon pour ses paysages naturels spectaculaires, son héritage culturel, ses festivals historiques, sa gastronomie et son accueil chaleureux, le Tōhoku offre un vent de fraîcheur au visiteur aventureux cherchant à fuir les foules, est n’est qu’à quelques heures au nord-est de la capitale par train à grande vitesse.
</p>
<p class="font_8" style="font-size:15px">​</span></p>

<h4 class="font_4" style="font-size:36px">4. Maine, USA</h4>
<p class="font_8" style="font-size:15px">
Le plus grand État de Nouvelle-Angleterre a toujours suscité la fierté, mais cet année, l’amour pour “Vacationland” va se muer en fièvre tandis que le Maine fêtera son bicentenaire. Dans toute la région, villes petites ou grandes présenteront expositions, concerts et festivals pour commémorer ces 200 ans en tant qu’État. Mais même si le Maine ne fêtait pas l’événement, ce serait une bonne période pour y aller. Sa gastronomie s’est épanouie depuis quelques années, et on trouvera restaurants “de la ferme à la table”, torréfacteurs de café, artisans boulangers et brasseries artisanales dans tout l’État. Et malgré sa popularité croissante, on échappe facilement à la foule dans ses immenses forêts ou sur sa côte pittoresque ponctuée de phares.</p>
<p class="font_8" style="font-size:15px">​</span></p>

<h4 class="font_4" style="font-size:36px">5. Lord Howe Island, Australie</h4>
<p class="font_8" style="font-size:15px">
Au milieu de nulle part, à 600 km de la côte australienne, cette île magnifique frappe immédiatement les sens et sa beauté à couper le souffle est classée au Patrimoine mondial. Deux hautes montagnes verdoyantes dominent un lagon de rêve et le récif de corail le plus méridional au monde. Des plages en forme de croissants parfaits et de splendides sentiers de randonnée à travers la forêt luxuriante offrent de superbes activités de plein air. L’isolement de cet ancien volcan en a fait un refuge de nombreuses espèces endémiques, et il accueille une riche avifaune. L’île est un brillant exemple de tourisme écoresponsable : elle n’accueille que 400 visiteurs à la fois, et ceux-ci sont encouragés à participer à toute une série de projets écologiques. L’isolement de Lord Howe, et sa petite taille en font une escapade idyllique.
</p>
<p class="font_8" style="font-size:15px">​</span></p>

<h4 class="font_4" style="font-size:36px">6. Province du Guizhou, Chine</h4>
<p class="font_8" style="font-size:15px">
Le Guizhou est une province en transition. Sa capitale, Guiyang, compte plus de 4 millions d’habitants (soit plus que Los Angeles, Rome et Berlin intra-muros). Le soir venu, des cuisines de rue s’installent partout et un bol de nouilles de riz épicées ne coûte que quelques centimes ; dans les ruelles, artisans, cafés et bars commencent à ouvrir boutique. Dans la campagne, les villages aux maisons de bois branlantes demeurent presque tels qu’il y a plusieurs siècles, mais commencent à développer des infrastructures touristiques. Le tout est relié par de nouvelles lignes de train à grande vitesse, et on peut aujourd’hui aller de Guiyang à Chongqing ou Kunming en quelques heures. Et les efforts de construction du pays ont donné naissance à des autoroutes et des ponts flambant neufs qui font de la circulation dans le Guizhou et dans ses montagnes mystiques une entreprise bien ancrée dans le XXIe siècle.
</p>
<p class="font_8" style="font-size:15px">​</span></p>

<h4 class="font_4" style="font-size:36px">7. Province de Cadix, Espagne</h4>
<p class="font_8" style="font-size:15px">
Grâce à une série de triomphes gastronomiques, la province de Cadix s’est avancée sous les feux de la rampe. Reine du Triangle du Xéres, Jerez de la Frontera a connu sa première étoile au guide Michelin en 2018 grâce au restaurant restaurant LÚ, Cocina y Alma de Juan Luis Fernández, et ses tabancos (mi-bars, mi-marchands de vin) du XXe siècle ont échappé à la disparition grâce à des nouveaux propriétaires entreprenants. Même le xérès est revenu à la mode ! À El Puerto de Santa María, l’Aponiente d’Ángel León est un des deux seuls restaurants avec trois étoiles au Michelin d’Andalousie. Alevante, dans l’hôtel Sancti Petri, autre restaurant de León , le talonne avec une étoile. Vejer de la Frontera s’est transformé en haut lieu de la gastronomie et de l’hôtellerie de charme. Cafés, restaurants et hébergements dernier cri donnent une énergie nouvelle à Cadix, que fondèrent les Phéniciens. Ajoutez-y de nouvelles liaisons aériennes vers Jerez, et vous aurez une région qui n’attend plus que vous.
</p>
<p class="font_8" style="font-size:15px">​</span></p>

<h4 class="font_4" style="font-size:36px">8. Nord-Est argentin</h4>
<p class="font_8" style="font-size:15px">
Avec la chute d’eau la plus imposante d’Amérique du Sud, une riche histoire régionale et des possibilités exceptionnelles d’observer la faune, le Nord-Est argentin devrait figurer sur tous les agendas. Bien sûr, il y a les fameuses chutes d’Iguazú, une des sept merveilles du monde naturel, mais le reste de cette région tempérée et accidentée vous emmènera loin des sentiers battus. Créé en 2018, le parc national d’Iberá devrait devenir une des plus belles destinations d’Argentine. Avoir restauré cette portion de nature sauvage a eu un impact plus que positif sur les localités alentour et c’est un modèle de réussite. La faune endémique y revient, que ce soient les aras chloroptères, les cerfs des pampas ou les jaguars. Et le pays reste très bon marché pour les visiteurs étrangers.
</p>
<p class="font_8" style="font-size:15px">​</span></p>

<h4 class="font_4" style="font-size:36px">9. Golfe de Kvarner, Croatie</h4>
<p class="font_8" style="font-size:15px">
Prise en sandwich entre les hauts lieux touristiques que sont la Dalmatie et l’Istrie, 
cette partie moins connue de la côte croate se bâtit discrètement depuis dix ans une réputation 
dans les domaines de la gastronomie et de la protection de l’environnement. Aujourd’hui, 
le golfe de Kvarner est prêt à jouer les premiers rôles, et sa principale agglomération, 
la ville portuaire industrieuse de Rijeka, tiendra celui de capitale européenne de la culture en 2020. 
Des espaces redessinés pour accueillir musées et centres culturels tiendront lieu de décor flambant neuf.
 Et dans les seconds rôles, mais prêtes à voler la vedette, les îles du golfe à la beauté éternelle et 
 les villes fortifiées historiques avec leur architecture vénitienne, leurs plages et leur charme immense. </p>
<p class="font_8" style="font-size:15px">​</span></p>

<h4 class="font_4" style="font-size:36px">10. Amazonie brésilienne </h4>

<p class="font_8" style="font-size:15px">L’Amazonie brésilienne est la nature dans ce qu’elle a de plus pur : un monde ancien, avec une faune si multicolore, une flore si luxuriante et sauvage qu’on en aurait presque des hallucinations. Cette jungle brumeuse abrite des plantes et des animaux parmi les plus rares au monde, et des communautés qui sont depuis des siècles les gardiennes de cette immensité verte. Avec le dérèglement climatique, la sauvegarde de l’Amazonie brésilienne est devenue une question primordiale. En 2020, une visite réfléchie et bien préparée dans la plus importante forêt du monde se fera avec le tourisme durable en tête, pour qu’elle profite en même temps aux communautés locales et à l’économie nationale, tout en soulignant la valeur culturelle et financière de la protection de la nature.<br />



