<p class="font_8" style="line-height:1.5em;font-size:30px">
INFOMATIONS CONCERNANT LA DÉLIVRANCE DE VISA PAR L'AMBASSADE
</p>
<p class="font_8" style="font-size:15px">
Durant la période de l’épidémie du Coronavirus, les demandeurs de visa sont priés de contacter directement l’organisme partenaire ou une agence de voyage agréée au Vietnam pour entamer les démarches de demande de visa auprès du Service d’Immigration du Vietnam. Après avoir obtenu l’autorisation de délivrance du visa de ce service, les demamdeurs peuvent venir à l’Ambassade du Vietnam avec la déclaration de votre état de santé pour l’apposition du visa.
</p>
<img src="8.jpg" alt="cuba" style="width:650px;height:321px;object-fit:cover;object-position:50% 50%"/>
<p class="font_8" style="font-size:15px">
<h4 class="font_4" style="font-size:36px">VISA TOURISTIQUE</h4>
<p class="font_8" style="font-size:15px">
Toute demande de visa touristique peut être présentée jusqu’à un an avant le début du voyage, directement à l’Ambassade ou par correspondance. Le visa sera valable à partir de la date que vous indiquez dans le formulaire.<br><br>

Pour une demande déposée directement à l’Ambassade (en main propre ou par une tierce personne), veuillez vous présenter aux heures d’ouverture, muni des pièces suivantes :<br><br>

Formulaire de demande de visa NA1 dûment rempli avec photo d’identité collée.<br>
Passeport original, (valable au moins encore un mois après la date de sortie du Vietnam).<br>
Moyen de règlement (espèces, carte bancaire ou chèque).<br><br>
Pour une demande par correspondance, veuillez envoyer votre dossier par courrier recommandé avec avis de réception à l’Ambassade du Vietnam à l’adresse: 61 Rue de Miromesnil, 75008 Paris.  Votre dossier doit se composer de :<br><br>

Formulaire de demande de visa NA1 dûment rempli avec photo d’identité collée.<br>
Passeport original (valable au moins encore un mois après la date de sortie du Vietnam).<br>
Chèque à l’ordre de l’Ambassade du Vietnam <br>
Moyen de retour: Enveloppe Chronopost ou Enveloppe non timbrée, joint d’un bordereau de recommandé sans avis de réception pré-rempli et non collé.<br><br>
Vous pouvez également demander un "visa volant" en envoyant par email ou par voie postale sans vous séparer de votre passeport:<br>

Formulaire de demande de visa NA1 dûment rempli avec 02 photo d’identité collée.<br>
Copie couleur du passeport (valable au moins encore un mois après la date de sortie du Vietnam).<br>
Chèque à l’ordre de l’Ambassade du Vietnam <br>
Moyen de retour: Enveloppe Chronopost ou Enveloppe non timbrée, joint d’un bordereau de recommandé sans avis de réception pré-rempli et non collé.<br><br>
Si vous souhaitez  envoyer la demande par la poste et récupérer le visa en personne ou par une tierce personne à l’Ambassade, veuillez nous le signaler par écrit. Le Service visa va vous tenir au courant de la disponibilité du visa dès que possible.<br><br><br>

Le délai de traitement est d’une semaine pour les visas de 30 jours (1 mois) et 10 jours pour les visas de 89 jours (trois mois) ou de 03 jours pour les visas de 30 jours et d'une semaine pour les visa de 89 jours en procédure express. En cas de besoin d’informations complémentaires, ces délais peuvent être prolongés sans avis préalable.Pour de plus amples informations, veuillez nous contacter au 01.44.14.64.00 (informations générales) ou 01.44.14.64.13 (suivi de dossier) ou nous écrire à info@ambassade-vietnam.fr ou ls3@ambassade-vietnam.fr.<br><br>

Horaires d’ouverture : Lundi à Jeudi: de 9h30 à 12h00 et de 14h00 à 17h00. Le retrait se fait uniquement les matins du lundi au jeudi de 9h00-12h00.<br>



