<div class="container">
<div class="row">
<div class="col-11 col-md-10 mx-auto px-md-0">
<div class="card overlapping-lg-card card-with-bars py-md-7 px-md-8 p-5">
<p class="font_8" style="line-height:1.5em;font-size:25px">
<strong>Aéroports : quelles mesures sanitaires devez-vous respecter ?</strong>
</p>

<p class="font_8" style="line-height:1.5em;font-size:15px">
Depuis l’arrivée de la pandémie de Covid-19, les aéroports sont devenus des lieux sensibles, où des centaines de voyageurs en provenance et à destination du monde entier peuvent se croiser chaque jour. Tour d’horizon des règles sanitaires qui y sont appliquées.
</p>

</div>
<img src="4.jpg" alt="cuba" style="width:948px;height:321px;object-fit:cover;object-position:50% 50%"/>
<p class="font_8" style="font-size:15px">​</span></p>
<p class="font_8" style="line-height:1.5em;font-size:25px">
<strong>Quelles sont les mesures barrières mises en place dans les aéroports ?</strong>
</p>

<p class="font_8" style="line-height:1.5em;font-size:15px">
A l’instar de la plupart des lieux recevant du public, les aéroports français imposent tous le port systématique du masque dans l’ensemble de leurs espaces, qu’ils soient intérieurs ou extérieurs. Une mesure qui s’applique également lors de l’embarquement et sur l’ensemble des vols actuellement. Des distributeurs de gel hydroalcoolique ont également été largement installés au sein des aéroports, ainsi que des signalisations visant à respecter la distanciation physique, dans les couloirs et les espaces d’attente. Certains cheminements à l’intérieur des aérogares ont également été revus, ainsi que les règles d’accès aux boutiques présentes dans les lieux.
</p>

<p class="font_8" style="font-size:15px">​</span></p>
<p class="font_8" style="line-height:1.5em;font-size:25px">
<strong>Existe-t-il certaines mesures spécifiques ?</strong>
</p>

<p class="font_8" style="line-height:1.5em;font-size:15px">
Au-delà des mesures barrières habituelles, des règles sanitaires bien particulières ont été instaurées selon les aéroports. L’accent a été mis sur la désinfection des espaces intérieurs, qu’il s’agisse des comptoirs d’accueil, des escalators, des poignées de portes ou des boutons d’ascenseur, mais également des bannettes dans lesquelles les passagers doivent déposer leurs affaires lors du contrôle de sécurité.
</p>
<p class="font_8" style="font-size:15px">​</span></p>

<p class="font_8" style="line-height:1.5em;font-size:15px">
Les aéroports ont également renforcé les procédures visant à limiter les contacts physiques, notamment pendant le contrôle de sécurité, lors duquel détecteurs de métaux, scanner et body scan sont privilégiés, ainsi que l’enregistrement des passagers sur bornes automatiques.

Dans certains aéroports, qui accueillent des vols en direction de l’étranger et de certains pays à risques, des mesures spécifiques ont été mises en place. C’est le cas notamment des aéroports de Paris, Orly et Charles-de-Gaulle, qui réalisent des contrôles de température avec un dispositif de caméras thermiques, pour les voyageurs qui atterrissent à Paris. Certaines compagnies aériennes peuvent également effectuer un contrôle de température au départ. 
</p>
<p class="font_8" style="line-height:1.5em;font-size:25px">
<img src="5.jpg" alt="cuba" style="width:948px;height:390px;object-fit:cover;object-position:50% 50%"/>
<p class="font_8" style="line-height:1.5em;font-size:15px">

<p class="font_8" style="font-size:15px">​</span></p>
<p class="font_8" style="line-height:1.5em;font-size:25px">
<strong>Est-il possible d’effectuer un test PCR dans l’aéroport ?</strong>
</p>

<p class="font_8" style="line-height:1.5em;font-size:15px">
Certains aéroports ont mis en place des centres de dépistage, proposant des tests antigéniques et PCR à leurs passagers. Au sein des aéroports d’Orly et Charles-de-Gaulle, les passagers munis de leurs billets, peuvent venir se faire tester jusqu’à 72 heures avant leur vol, en prenant rendez-vous sur la plateforme Doctolib. À Lyon, un centre de dépistage Covid-19 a également été mis en place et est accessible sans rendez-vous, au sein du terminal 2, pour les passagers au départ ou à l’arrivée.

</p>
<p class="font_8" style="font-size:15px">​</span></p>

