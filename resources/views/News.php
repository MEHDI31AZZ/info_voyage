<p class="font_8" style="line-height:1.5em;font-size:30px">
Les 5 plus belles plages de Grèce
</p>
<p class="font_8" style="font-size:15px">
L'épidémie de COVID-19 et les restrictions de voyage ont réduit à néant de nombreux projets de vacances. Mais il pourrait être possible de voyager à nouveau d'ici quelques temps. Nous avons préparé un guide des meilleures plages de Grèce pour profiter du soleil lorsque cela devient possible.

En cette période difficile, nous rêvons tous de prendre du soleil sur une plage paradisiaque. Bien que nous ne puissions pas encore faire ce voyage, nous pouvons commencer à créer une liste de destinations de rêve à visiter dans le futur – comme ces incroyables plages grecques.
</p>
<img src="6.jpg" alt="cuba" style="width:650px;height:321px;object-fit:cover;object-position:50% 50%"/>
<p class="font_8" style="font-size:15px">
<h4 class="font_4" style="font-size:36px">1. La plage Myrtos, Céphalonie, Îles ioniennes</h4>
<p class="font_8" style="font-size:15px">
Située sur l’île de Céphalonie dans les îles Ioniennes, la plage de Myrtos est sans aucun doute l’une des plus belles plages de Grèce. Située au pied de deux imposantes montagnes qui culminant à 901 mètres d’altitude, cette longue plage de galets blancs ne manque pas d’atouts avec ses eaux turquoises et ses falaises plongeant à même l’eau. Si vous visitez la plage le soir, vous pourrez y admirer de superbes couchers de soleil et vivre pleinement votre séjour sur une île grecque.</p>

<h4 class="font_4" style="font-size:36px">2. La plage Mpalos (Crète)</h4>
<p class="font_8" style="font-size:15px">
La Crète est l’une des plus belles îles grecques. Située à la pointe Nord-Ouest de la Crète, la plage de Mpalos est difficile à décrire, tant l’émotion est difficile à retranscrire à l’écrit. On va quand même essayer de décrire celle qui figure parmi les plus belles plages de Grèce. Imaginez une baie peu profonde remplie d’eau turquoise délicieusement chauffée par le soleil grec, et entourée d’une nature sauvage préservée de l’urbanisation sauvage… Imaginez tout autour de cette crique une magnifique plage de sable blanc, où vous pourrez bronzer en toute tranquillité loin des sentiers battus du tourisme de masse. Bienvenue à la plage Mpalos en Crète.

Certes, la plage Mpalos est relativement difficile d’accès : il vous faudra rouler sur une petite route sinueuse avant de finir le trajet à pied pour la rejoindre. Mais cet effort en vaut vraiment la peine et cela fait pleinement partie d’un voyage dans les îles grecques !
</p>

<h4 class="font_4" style="font-size:36px">3. Plage Voutoumi, Antipaxos (Îles Ioniennes)</h4>
<p class="font_8" style="font-size:15px">
Aimez-vous les lagons des Caraïbes remplies d’eaux turquoise ? Aimez-vous les grottes marines ? Si oui, rendez-vous sur l’île d’Antipaxos, une petite île de l’archipel Ionien encore vierge – car l’île est relativement peu touristique et peuplée d’à peine 64 habitants.

Située à 2,5 km au sud-est de Paxos, vous pourrez rejoindre l’île depuis Paxos en bateau, le trajet durant 15 minutes environ. Figurant parmi les plus belles îles grecques, cette île vous dévoile des plages d’exception.

<h4 class="font_4" style="font-size:36px">4. La plage volcanique de Nea Kameni, Santorin</h4>
<p class="font_8" style="font-size:15px">
Vous êtes vous déjà baigné dans le cratère d’un volcan ? Si la réponse est non, rendez-vous sur le petit îlot de Nea Kameni, situé au centre de la caldeira de Santorin durant votre séjour sur les îles grecques. Une fois sur place, vous pourrez vous baigner dans les sources d’eau chaude sous-marine qui chauffent la mer à une température comprise entre 35 et 45°C. Certes, la mer sent un peu le soufre, mais l’expérience est enrichissante. Une fois revenu de votre excursion à Nea Kameni, vous pourrez rejoindre la ville de Thira – à dos d’âne, en téléphérique ou à pied pour les plus téméraires d’entre vous – pour y admirer la formidable vue sur l’île, les plages de Santorin qui figurent parmi les plus belles plages de Grèce et le volcan en contrebas.

<h4 class="font_4" style="font-size:36px">5. Plage de Perissa (île de Santorin)</h4>
<p class="font_8" style="font-size:15px">
Encore une plage à Santorin ! Située à 12 kilomètres au sud-est de Thira – le chef-lieu de Santorin – la plage de Perissa est une magnifique plage unique en son genre. Longue de 7 kilomètres, la plage de Perissa est composée de sable noir – Santorin étant une île grecque d’origine volcanique. La plage est également réputée pour sa très belle vue sur la montagne Mesa Vouno.

Elle abrite de nombreux restaurants et cafés, finissez la journée en prenant un apéritif sur la terrasse d’un restaurant. Vous pouvez en profiter pour déguster du choloro grillé – le fromage local – du koufeto, sorte d’amandes baignant dans du miel bouilli, ou encore vous rafraîchir les idées en buvant de l’ouzo avec modération.


